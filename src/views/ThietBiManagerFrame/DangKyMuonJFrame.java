    private boolean checkMaLop(String MaLop) {
        if (MaLop.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập mã lớp", "Warning", JOptionPane.WARNING_MESSAGE);
            return false;
        } else {
            try {
                long temp = Long.parseLong(MaLop);
                if (MaLop.trim().length() != 6) {
                    throw new Exception("Sai dinh dang");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đúng định dạng mã lớp!", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }
        return true;
    }
